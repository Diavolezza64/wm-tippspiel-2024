def pts_in(cum, m, start, end, n):
    end = min(end, n) if end else n
    if start > n or start > end: return 0
    return cum[m][end-1] - (cum[m][start-2] if start > 1 else 0)

def ranking(members, cum, n, start, end):
    pl = sorted(((m, pts_in(cum, m, start, end, n)) for m in members), key=lambda x:-x[1])
    res, prev_p, prev_r = [], None, 0
    for i,(m,p) in enumerate(pl):
        if p != prev_p: prev_r, prev_p = i+1, p
        res.append((prev_r, m, p))
    return res

def _round_frac(v):
    """Rundenname/Antwort → Kurzform (1/8, 1/4, Ländername, Zahl usw.)"""
    if v is None: return '?'
    if not isinstance(v, str): return str(v)
    r = v.lower()
    if 'sechzehntel'        in r: return '1/16'
    if 'achtel'             in r: return '1/8'
    if 'viertel'            in r: return '1/4'
    if r.startswith('halbfinal') or 'halbfinale' in r: return '1/2'
    if r == 'final' or 'finale' in r: return '1/1'
    if 'sieger'             in r: return 'Sieger'
    if 'gruppe' in r or 'vorrunde' in r: return 'GP'
    return v  # Klartext (Ländername, Zahl etc.)


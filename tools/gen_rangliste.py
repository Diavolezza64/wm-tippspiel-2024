def _parse_csv_text(text):
    reader = csv.reader(io.StringIO(text), delimiter=';')
    header = next(reader)
    rows = [r for r in reader if r and r[0].strip()]
    members, col_pts = [], {}
    for i, h in enumerate(header):
        h = h.strip().strip('"')
        if h.endswith(' Punkte kum.'):
            name = h[:-len(' Punkte kum.')].strip()
            members.append(name)
            col_pts[name] = i
    cum = {m: [] for m in members}
    for row in rows:
        for m in members:
            v = row[col_pts[m]].strip().strip('"')
            cum[m].append(int(v) if v else 0)
    return members, cum, len(rows)

